function oppositeNum(num) {
    return -num;
    }
console.log(oppositeNum(10));  // Output: -10
console.log(oppositeNum(-5));  // Output: 5
console.log(oppositeNum(0));   // Output: 0
console.log(oppositeNum(100)); // Output: -100